		<h4>Sejarah Himpunan Mahasiswa Teknik Informatika</h4><br>
		<p>
			Himpunan Mahasiswa Teknik Informatika (HMIF) Beridiri pada tanggal 15 Maret 2005 oleh Rendi Mawardi dkk sekaligus sebagai Ketua Himpunan pertama. VIsi awal didirikannya HMIF adalah dibutuhkannya membuat suatu wadah mahasiswa informatika, rekan rekan IF pada saat itu ber-koordinasi dengan jurusan. Pada saat itu, langsung didukung penuh oleh Pak Winarno Sugeng yang menjabat Ketua Jurusan.
		</p>

		<br>
		<br><p>
			Pemilihan Ketua Himpunan (Kahim) dan sekertaris kahim berlangsung, dengan dilakukan dengan pemungutan suara terbanyak, disaksikan oleh beberapa orang dari jurusan IF ( termasuk Pak Winarno ) dan juga ketua angkatan 2004 ( Asep Septriyana ).
			<br>
				Segala kondisi tekait pemenuhan kebutuhan guna tebentuknya suatu himpunan yang “utuh” yang dilakukan pada saat itu, terutama pembuatan AD/ART Himpunan dan Struktur Organisasi. Terbentuknya AD/ART, banyak melakukan komunikasi dan referensi dari rekan rekan Himpunan Teknik Elektro. Selain itu, tidak lupa kita juga sering ber-koordinasi dan mencari bahan referensi lainnya diluar kampus, semisal : ITB (Informatika, Planologi), Unpad (Hukum). AD/ART Himpunan IF terbentuk dengan tetap berdasarkan kepara kebutuhan mahasiswa IF itu sendiri.
				</p>

				<br><br>
				<p>Selanjutnya Himpunan IF dikenal dengan HIMPUNAN MAHASISWA TEKNIK INFORMATIKA ITENAS, yang disingkat dengan HMIF ITENAS. Berkenaan dengan inisiasi berdirinya HMIF, dilakukan berbagai konsolidasi ke berbagai pihak didalam Kampus Itenas, diantaranya : Pihak Dekan Rektor dan juga Keluarga Mahasiswa Itenas (KM). dalam waktu singkat HMIF telah diakui oleh Itenas dan Juga mendapatkan dan himpunan pertamanya untuk menajalankan Program Kerja pada saat itu : Pembuatan Seminar, Kunjungan Industri dan Program dukungan Jurusan untuk Penerimaan Mahasiswa Baru.
					</p>

